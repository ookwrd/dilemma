package world;

public class Perception {
	double signalIn;
	double health;
	double messageIn;
	double faceIn; //optional
	
	public double getSignalIn() {
		return signalIn;
	}
	public void setSignalIn(double signalIn) {
		this.signalIn = signalIn;
	}
	public double getHealth() {
		return health;
	}
	public void setHealth(double health) {
		this.health = health;
	}
	public double getMessageIn() {
		return messageIn;
	}
	public void setMessageIn(double messageIn) {
		this.messageIn = messageIn;
	}
	public double getFaceIn() {
		return faceIn;
	}
	public void setFaceIn(double faceIn) {
		this.faceIn = faceIn;
	}
	
}